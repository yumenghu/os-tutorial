; 一个最简单的引导扇区程序，功能是无限循环（挂起）
; loop 是一个标签，代表当前位置的内存地址（在汇编中用于跳转）
loop:
    jmp loop        ; 无条件跳转到 loop 标签，实现死循环

; 用 0 填充代码，使得总大小达到 510 字节（$ 为当前位置，$$ 为程序起始位置）
; ($ - $$) 计算当前代码所占用的字节数（即上面 jmp loop 占用了几个字节）
; times 会执行指定次数，所以这里是填充 [510 - 当前代码大小] 个 0
times 510 - ($ - $$) db 0

; 引导扇区魔数（magic number）：BIOS 会检查最后两个字节是否是 0x55AA，否则不会认为这是一个可引导的扇区
; 注意：由于 x86 是小端序（little-endian），写成 dw 0xaa55 实际内存布局是 0x55, 0xAA（顺序没错）
dw 0xaa55
